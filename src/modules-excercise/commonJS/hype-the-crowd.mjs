// hype-the-crowd.mjs
export default function hypeTheCrowd(call, response) {
  console.log(`When I say ${call}, you say ${response}!`)
  console.log(`ME: ${call.toUpperCase()}!`)
  console.log(`CROWD: ${response.toUpperCase()}!`)
  console.log(`ME: ${call.toUpperCase()}!`)
  console.log(`CROWD: ${response.toUpperCase()}!`)
  console.log(`ME: I can't hear you!`)
}
